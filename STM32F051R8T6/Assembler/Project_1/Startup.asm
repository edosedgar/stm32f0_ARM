TIM6_DAC_IRQHandler PROC
	
	LDR R0,=GPIOA+IDR
	LDR R1,[R0]
	LDR R0,=0x0001
	ANDS R1,R1,R0
	BEQ SkipBLinkLed 
	
	LDR R0,=GPIOC+ODR
	LDR R1,[R0]
	LDR R2,=0x0100
	EORS R2,R2,R1
	STR R2,[R0]

SkipBLinkLed	
	WriteIn TIM6+TIM6_SR,0x0000	
	WriteIn ICPR,0x20000	
	BX LR
	ENDP
